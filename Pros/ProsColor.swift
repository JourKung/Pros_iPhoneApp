import UIKit

extension UIColor {
    
    // ------------------------------
    // MARK: -
    // MARK: Configuration
    // ------------------------------
    
    class func applicationGreenColor() -> UIColor {
        return UIColor(red: 0.255, green: 0.804, blue: 0.470, alpha: 1)
    }
    
    class func applicationBlueColor() -> UIColor {
        return UIColor(red: 0.333, green: 0.784, blue: 1, alpha: 1)
    }
    
    class func applicationPurpleColor() -> UIColor {
        return UIColor(red: 0.659, green: 0.271, blue: 0.988, alpha: 1)
    }
    
    class func applicationOrangeColor() -> UIColor {
        return UIColor(red: 241, green: 131, blue: 33, alpha: 1)
    }
    
    class func applicationDarkOrangeColor() -> UIColor {
        return UIColor(red: 110, green: 63, blue: 21, alpha: 1)
    }
    
    class func applicationDarkBlueColor() -> UIColor {
        return UIColor(red: 10, green: 132, blue: 171, alpha: 1)
    }
    
    class func applicationBlue2Color() -> UIColor {
        return UIColor(red: 98, green: 144, blue: 218, alpha: 1)
    }
    
    class func applicationMercuryColor() -> UIColor {
        return UIColor(red: 230, green: 230, blue: 230, alpha: 1)
    }
}